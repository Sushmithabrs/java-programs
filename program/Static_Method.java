package program;
//WAP with parameterized static method using char and Boolean as parameters
public class Static_Method {
	static void a1 (boolean a, boolean b) {
		System.out.println("boolean value");
	}
	static void ab1 (char a, char b) {
		System.out.println("char value");
	}


	public static void main(String[] args) {
	a1(true,true);
	ab1('f','m');
	}

}
